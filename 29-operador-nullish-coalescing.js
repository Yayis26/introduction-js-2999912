//Operador nullish coalescing Sirve para dar un valor por defecto cuando algo es nullo o undefined 

//Estructura: valor1 ?? valor2 

//1- Si valor1 no es nulo ni undefined se usa valor1 
//2- Si valor1 es nulo o undefined se usa valor2

//Ejemplo 

let userName = "Dayana";
let user = userName ?? "Invited";
console.log(user); 